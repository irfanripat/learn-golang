package main

import "fmt"

func main() {
	var name string

	name = "Irfan Ripat"
	fmt.Println(name)

	name = "John Doe"
	fmt.Println(name)

	country:= "Indonesia"
	fmt.Println(country)

	country = "Netherland"
	fmt.Println(country)

	var (
		firstName = "Irfan"
		lastName = "Ripat"
	)

	fmt.Println(firstName, lastName)
}